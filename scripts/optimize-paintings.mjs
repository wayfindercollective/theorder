/**
 * optimize-paintings.mjs — generate lean WebP derivatives of the full-bleed
 * paintings the public homepage loads as CSS backgrounds / the hero <img>.
 *
 * The source oils are 2560px, 0.9–1.5 MB JPEGs; ~13 MB decode on every hard
 * reload. They're shown full-bleed but heavily dimmed, masked and colour-graded,
 * so a 2000px-long-edge WebP q80 is visually identical and a fraction of the
 * bytes / decoded memory. Originals are left untouched — derivatives land in
 * public/images/opt/ and the app routes to them via src/lib/img.js when the
 * basename is listed in the generated manifest (src/lib/optimizedImages.js).
 * CMS-safe: a freshly uploaded image with no derivative simply serves as-is.
 *
 *   node scripts/optimize-paintings.mjs
 */
import sharp from 'sharp'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const IMG = path.join(root, 'public', 'images')
const OUT = path.join(IMG, 'opt')

// Per-image overrides; everything else uses the default cap/quality.
const DEFAULT = { maxLong: 2000, quality: 80 }
const OVERRIDES = {
  // the hero is the LCP + shown at full viewport height — keep it a touch larger
  'hero-horseman': { maxLong: 2400, quality: 82 },
}

// Collect every /images/*.{jpg,png} the site references from sections.json,
// minus the social-share meta image (crawler-only, never rendered on-page).
function referenced() {
  const json = JSON.parse(fs.readFileSync(path.join(root, 'content', 'sections.json'), 'utf8'))
  const found = new Set()
  const walk = (o) => {
    if (!o) return
    if (typeof o === 'string') {
      const m = o.match(/^\/images\/([^/?#]+)\.(jpe?g|png)$/i)
      if (m) found.add(m[1])
    } else if (typeof o === 'object') {
      for (const k of Object.keys(o)) walk(o[k])
    }
  }
  walk(json)
  found.delete('og-share')
  return [...found].sort()
}

async function run() {
  fs.mkdirSync(OUT, { recursive: true })
  const names = referenced()
  const done = []
  let srcBytes = 0
  let outBytes = 0

  for (const name of names) {
    const src = ['jpg', 'jpeg', 'png'].map((e) => path.join(IMG, `${name}.${e}`)).find(fs.existsSync)
    if (!src) {
      console.warn(`skip ${name} — no source file`)
      continue
    }
    const { maxLong, quality } = OVERRIDES[name] || DEFAULT
    const dst = path.join(OUT, `${name}.webp`)
    const meta = await sharp(src).metadata()
    const long = Math.max(meta.width, meta.height)
    const resize = long > maxLong
      ? (meta.width >= meta.height ? { width: maxLong } : { height: maxLong })
      : null

    let pipe = sharp(src)
    if (resize) pipe = pipe.resize(resize)
    await pipe.webp({ quality, effort: 5 }).toFile(dst)

    const s = fs.statSync(src).size
    const o = fs.statSync(dst).size
    srcBytes += s
    outBytes += o
    done.push(name)
    const dm = await sharp(dst).metadata()
    console.log(
      `${name.padEnd(20)} ${meta.width}x${meta.height} ${(s / 1024 | 0)}KB  →  ${dm.width}x${dm.height} ${(o / 1024 | 0)}KB webp`
    )
  }

  // Write the manifest the runtime helper consults.
  const manifest =
    `// AUTO-GENERATED by scripts/optimize-paintings.mjs — do not edit by hand.\n` +
    `// Basenames (no extension) that have an optimized WebP in /images/opt/.\n` +
    `export const OPTIMIZED = new Set(${JSON.stringify(done.sort(), null, 2)})\n`
  fs.writeFileSync(path.join(root, 'src', 'lib', 'optimizedImages.js'), manifest)

  console.log('-'.repeat(60))
  console.log(
    `${done.length} images:  ${(srcBytes / 1048576).toFixed(2)} MB  →  ${(outBytes / 1048576).toFixed(2)} MB webp` +
    `  (${(100 - (outBytes / srcBytes) * 100).toFixed(0)}% smaller)`
  )
}

run().catch((e) => {
  console.error(e)
  process.exit(1)
})
